int	max(int *tab, int len)
{
	int	max;
	int	i;

	max = 0;
	i = 0;
	if (len == 0)
		return (0);
	while (i < len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
